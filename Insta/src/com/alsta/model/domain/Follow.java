package com.alsta.model.domain;

public class Follow {
	private int follow_id;
	private int me;
	private int you;
	
	public int getFollow_id() {
		return follow_id;
	}
	public void setFollow_id(int follow_id) {
		this.follow_id = follow_id;
	}
	public int getMe() {
		return me;
	}
	public void setMe(int me) {
		this.me = me;
	}
	public int getYou() {
		return you;
	}
	public void setYou(int you) {
		this.you = you;
	}
	
	

}
